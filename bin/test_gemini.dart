import 'dart:convert';
import 'package:http/http.dart' as http;

// Quiz Models
class Quiz {
  Quiz({
    required this.id,
    required this.title,
    required this.description,
    required this.questions,
    required this.createdAt,
    required this.source,
  });

  final String id;
  final String title;
  final String description;
  final List<QuizQuestion> questions;
  final DateTime createdAt;
  final String source;

  Map<String, dynamic> toJson() => {
        'id': id,
        'title': title,
        'description': description,
        'questions': questions.map((q) => q.toJson()).toList(),
        'createdAt': createdAt.toIso8601String(),
        'source': source,
      };
}

class QuizQuestion {
  QuizQuestion({
    required this.id,
    required this.question,
    required this.options,
    required this.correctAnswer,
    required this.explanation,
  });

  final String id;
  final String question;
  final List<String> options;
  final int correctAnswer;
  final String explanation;

  Map<String, dynamic> toJson() => {
        'id': id,
        'question': question,
        'options': options,
        'correctAnswer': correctAnswer,
        'explanation': explanation,
      };
}

class QuizConfig {
  QuizConfig({
    this.numQuestions = 10,
    this.difficulty = 'Medium',
    this.quizType = 'Multiple Choice',
  });

  final int numQuestions;
  final String difficulty;
  final String quizType;
}

// Gemini Service
class GeminiService {
  final String apiKey;
  final String baseUrl = 'https://generativelanguage.googleapis.com/v1beta/models/gemini-3.1-pro-preview:generateContent';

  GeminiService({required this.apiKey});

  Future<Quiz?> generateQuizFromText({
    required String content,
    required QuizConfig config,
    required String source,
  }) async {
    try {
      final prompt = _buildQuizPrompt(content, config);
      
      final response = await http.post(
        Uri.parse('$baseUrl?key=$apiKey'),
        headers: {'Content-Type': 'application/json'},
        body: jsonEncode({
          'contents': [
            {
              'parts': [
                {'text': prompt}
              ]
            }
          ],
          'generationConfig': {
            'temperature': 0.7,
            'topK': 40,
            'topP': 0.95,
            'maxOutputTokens': 4096,
          }
        }),
      ).timeout(const Duration(seconds: 30));

      if (response.statusCode == 200) {
        final data = jsonDecode(response.body);
        final text = data['candidates'][0]['content']['parts'][0]['text'] as String;
        return _parseQuizFromResponse(text, source);
      } else {
        print('Gemini API Error: ${response.statusCode}');
        print('Response: ${response.body}');
        return null;
      }
    } catch (e) {
      print('Quiz Generation Error: $e');
      return null;
    }
  }

  String _buildQuizPrompt(String content, QuizConfig config) {
    return '''
Generate a ${config.quizType} quiz based on the following content.

Requirements:
- Number of questions: ${config.numQuestions}
- Difficulty level: ${config.difficulty}
- Question type: ${config.quizType}
- Each question should have 4 multiple choice options
- Include explanations for correct answers

Content:
$content

Format your response EXACTLY as a JSON array like this:
[
  {
    "question": "What is...",
    "options": ["Option A", "Option B", "Option C", "Option D"],
    "correctAnswer": 0,
    "explanation": "The correct answer is Option A because..."
  }
]

Generate only the JSON array, no other text.
''';
  }

  Quiz? _parseQuizFromResponse(String text, String source) {
    try {
      final jsonMatch = RegExp(r'\[[\s\S]*\]').firstMatch(text);
      if (jsonMatch == null) {
        print('Could not find JSON in response');
        return null;
      }

      final jsonStr = jsonMatch.group(0)!;
      final parsed = jsonDecode(jsonStr) as List;

      final questions = <QuizQuestion>[];
      for (int i = 0; i < parsed.length; i++) {
        final q = parsed[i] as Map<String, dynamic>;
        questions.add(QuizQuestion(
          id: 'q_$i',
          question: q['question'] as String,
          options: List<String>.from(q['options'] as List),
          correctAnswer: q['correctAnswer'] as int,
          explanation: q['explanation'] as String? ?? 'No explanation',
        ));
      }

      return Quiz(
        id: 'quiz_${DateTime.now().millisecondsSinceEpoch}',
        title: 'Quiz from $source',
        description: 'Generated by Gemini AI',
        questions: questions,
        createdAt: DateTime.now(),
        source: source,
      );
    } catch (e) {
      print('Parse Quiz Error: $e');
      return null;
    }
  }
}

